num=int(input("Digite um número: "))  #Usuário informa o número 

if num > 0:   #Verifica se o número é maior que 0 
    print("O número digitado é Positivo.")  #Se maior que 0, ele é positivo
else:
    if num == 0: #Verifica se ele é igual a 0
        print("O número digitado não é nem positivo e nem negativo, ele é 0.") #Se igual a 0, mostrar que ele não é nem positivo e nem negativo, mas sim 0
    else:  #Se o número não for nem maior e nem igual a 0. Ele é negativo
        print("O número digitado é Negativo.")

